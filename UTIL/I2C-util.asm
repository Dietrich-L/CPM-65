	LDY #C_I2CGTIME
	JSR JBIOS
	BCS TIMEX	;display Time
	LDA #TIME_M
	LDY #TIME_M/256
	JSR PRTSTR
	LDA HOUR
	AND #$3F	;clear 12/24h & AM/PM bits
	JSR PRTHEX
	LDA #':
	JSR PRTCHR
	LDA MIN
	JSR PRTHEX
	LDA #':
	JSR PRTCHR
	LDA SEC
	JSR PRTHEX
	JSR CRLF	
	CLC
