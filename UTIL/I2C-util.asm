I2CUTIL	JSR INIT 	;INIT VARIABLES

IUTILL	JSR STATUS
	LDA #PROMPT
	LDY #PROMPT/256
	JSR PRTSTR
	LDX #GETLIN
	JSR BDOS
	LDY #0		;SET COMMAND POINTER
	STY CMDVEC
IUTIL1	JSR DECODE	;DECODE AND EXEC COMMAND
	BCC IUTIL1
	CMP #EOL 	;END OF COMMAND
	BEQ IUTILL
	JSR ERROR
	JMP IUTILL


INIT	LDA #0		;RESET VARIABLES
	LDX #$30
INIT2	STA 0,X
	DEX
	BPL INIT2
	LDA PADD	
	ORA #%00000001	; PA0 is Output
	STA PADD
	LDY #C_I2CCLR	;clear I2C
	JSR JBIOS
	RTS


STATUS	LDA #STATM	;DISPLAY STATUS
	LDY #STATM/256
	JSR PRTSTR
	RTS


PRTDEC	JSR COMNUM	;PRINTS DECIMAL
	PHA
	TYA
	JSR COMNUM
	PHA
	TYA
	JSR PRTNUM
	PLA
	JSR PRTNUM
	PLA
	JSR PRTNUM
	RTS


COMNUM	LDY #0		;DIV/10
COMNU1	SEC
	SBC #10
	BCC COMNU2
	INY
	BNE COMNU1

COMNU2	ADC #10
	RTS


PRTNUM	ORA #$30 	;PRINT A NUMBER
	JMP PRTCHR


DECODE	JSR GNCHR	;GET NEXT CHR
	BCS DECODX
	CMP #SEMI	;IS IT A SEPARATOR
	BNE DECOD1
	JSR GNCHR
	BCS DECODX
DECOD1	LDY #0		;SEARCH COMMAND
	STA CMDKEY
DECOD4	LDA CMDTAB,Y
	BEQ DECOD2	;COMMAND NOT FOUND
	CMP CMDKEY
	BEQ DECOD3	;FOUND
	INY
	INY
	INY
	BNE DECOD4

DECOD3	INY
	LDA CMDTAB,Y
	PHA
	INY
	LDA CMDTAB,Y
	PHA
	RTS		;EXEC

DECOD2	LDA #NOCOM	;UNKNOWN COMMAND
	SEC
DECODX	RTS


;==========  COMMANDS  ============= 


