	STA RCOUNT
	BNE REPEA3

REPEA5	LDA #EOL
	SEC
	RTS


;======  SUBROUTINES  ============== 

R_BLK	LDA YBUF		;Read Block to Buffer
	LDY #C_I2CADEV		;select dev for Write
	JSR JBIOS
	BCS RBLKX
	LDA #CURAD		;set 16 bit reg
	LDY #C_I2CR16
	JSR JBIOS
	BCS RBLKX
	LDA YBUF
	LDY #C_I2CRDEV
	JSR JBIOS
	BCS RBLKX
RBLK1	JSR CHKEND		;read next byte
	BCS RBLK2
	LDY #C_I2CRByte
	JSR JBIOS
	BCS RBLKX
	LDY #0
	STA (PNT),Y
	INC PNT
	BNE RBLK1
	INC PNT+1
	BNE RBLK1

RBLK2	LDY #C_I2CRLB		;dummy read last byte
	JSR JBIOS
RBLKX	RTS



WPAGE	LDA YBUF		;write 32 byte page
	LDY #C_I2CADEV		;select dev for Write
	JSR JBIOS
	BCS WPAGX
	LDA #CURAD		;set 16 bit reg
	LDY #C_I2CR16
	JSR JBIOS
	BCS WPAGX
WPAGE1	LDY #0
	LDA (PNT),Y
	LDY #C_I2CWBYTE
	JSR JBIOS
	BCS WPAGX
	INC CURAD		;inc CURAD
	BNE WPAGE5
	INC CURAD+1
WPAGE5	INC PNT			;inc PNT
	BNE WPAGE3
	INC PNT+1
WPAGE3	JSR CHKEND		;PNT >= ENDAD
	BCC WPAGE4
	LDY #C_I2CSTOP
	JSR JBIOS
	LDA YBUF
	LDY #C_I2CADEV		;wait till ready
	JSR JBIOS
WPAGX	RTS

WPAGE4	LDA CURAD
	AND #$1F		;max. 32 bytes
	BNE WPAGE1
	LDY #C_I2CSTOP		;wait till ready
	JSR JBIOS
	LDA YBUF
	LDY #C_I2CADEV
	JSR JBIOS
	BCC WPAGE
	RTS


ADDHEAD	CLC			;set reg to start of data
	LDA CURAD
	ADC #4			;+header
	STA CURAD
	BCC ADDHDX
	INC CURAD+1
ADDHDX	RTS


