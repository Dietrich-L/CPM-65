DATE	LDA #DAY	;get date&time info
	LDY #C_I2CGDATE
	JSR JBIOS
	BCS DATEX	;display date
	LDA #DATE_M
	LDY #DATE_M/256
	JSR PRTSTR
	LDA DAY
	JSR Weekday
	LDA DD
	JSR PRTHEX
	LDA #'.
	JSR PRTCHR
	LDA MM
	AND #$7F	;clear century bit
	JSR PRTHEX
	LDA #'.
	JSR PRTCHR
	LDA #$20
	JSR PRTHEX
	LDA YY
	JSR PRTHEX
	JSR CRLF
	CLC
DATEX	RTS


LOAD_ID	JSR GETHEX	;load ID data set to buffer
	BCS LOADIDX
	AND #$7F	;7 bit dev in DT
	STA DT
	JSR GETHEX
	BCS LOADIDX
	STA DT+1	;ID in DT+1
	STY DT+2
LOADID3	LDA BUFFER	;BUFFER in DT+3
	STA DT+3
	LDA BUFFER+1
	STA DT+4
	LDA #DT
	LDY #C_I2CLOADID
	JSR JBIOS
LOADIDX	RTS


SET	JSR G1ADR	;Set adr, reg, byte
	BCS SETX
	AND #$7F	;bits 7 must be 0
	STA PNT		;adr
	JSR GETHEX
	BCS SETX
	STA PNT+1	;register byte
	JSR GETHEX
	BCS SETX
	STA PNT+2	;value
	LDA PNT
	LDY #C_I2CADEV	;select dev for Write
	JSR JBIOS
	BCS SETX
	LDA PNT+1
	LDY #C_I2CWByte	;set register to write
	JSR JBIOS
	BCS SETX
	LDA PNT+2
	LDY #C_I2CWBYTE	;write byte to reg
	JSR JBIOS
	BCS SETX
	LDY #C_I2CSTOP
	JSR JBIOS
SETX	RTS


READ	JSR G1ADR	;read from adr, reg word,
	BCS READX	;nnnn bytes to buffer
	AND #$7F	;bits 7 must be 0
	STA YBUF	;save dev
	JSR GETHEX
	BCS READX
	STA CURAD	;CURAD = reg
	STY CURAD+1
	JSR GETHEX
	BCS READX
	STA ENDAD
	STY ENDAD+1
	LDA BUFFER	;set PNT & ENDAD
	STA PNT
	ADC ENDAD
	STA ENDAD
	LDA BUFFER+1
	STA PNT+1
	ADC ENDAD+1
	STA ENDAD+1
	JSR R_BLK	;read block
READX	RTS


WRITE	JSR G1ADR	;write to adr, reg word,
	BCS WRITEX	;nnnn bytes from buffer
	AND #$7F	;bits 7 must be 0
	STA YBUF	;save dev
	JSR GETHEX	;get reg
	BCS READX
	STA CURAD
	STY CURAD+1
	JSR GETHEX	;get n
	BCS WRITEX
	STA ENDAD
	STY ENDAD+1
	LDA ENDAD	;check for nnnn = 0
	BNE WRITE1
	LDA ENDAD+1
	BNE WRITE1
	LDA #ARG0	;arg = 0 Error
WRITEX	SEC
	RTS

WRITE1	LDA BUFFER	;set BEGAD & ENDAD
	STA PNT
	ADC ENDAD
	STA ENDAD
	LDA BUFFER+1
	STA PNT+1
	ADC ENDAD+1
	STA ENDAD+1
WRITE2	JSR WPAGE
	RTS


INFO	LDY #C_I2CCLR
	JSR JBIOS
	BCS INFOX
	LDA #INFO_M
	LDY #INFO_M/256
	JSR PRTSTR
	LDA #0
	STA YBUF	;start with dev = 0
INFO3	JSR PRT2SP	;print dev adresses
	LDA YBUF
	JSR PRTHEX
	LDA YBUF
	LDY #C_I2CADEV
	JSR JBIOS
	BCC INFO1
	LDA #CR
	JSR PRTCHR
INFO2	INC YBUF
	BPL INFO3
	JSR CRLF	;all adr scanned
	CLC
INFOX	RTS

INFO1	JSR PRT2SP	;print dev adresses
	LDA YBUF
	ASL A
	JSR PRTHEX
	JSR PRT2SP
	LDA YBUF
	SEC
	ROL A
	JSR PRTHEX
	JSR CRLF
	JMP INFO2


SETBUF	JSR GETHEX	;GET BUFFER ADDRESS
	BCS SETBUX
	STY BUFFER+1
	STA BUFFER
SETBUX	RTS


CHANGE	JSR GNCHR	;GET NEXT CHR
	BCS CHANGX
	CMP #'H
	BEQ CHEX
	CMP #'A
	BEQ CASC
	LDA #ILLPAR	;ILLEGAL PARAMETER
CHANGX	SEC
	RTS

CHEX	JSR GETHEX	;GET Memory POINTER
	BCS CHANGX
	STY PNT+1
	STA PNT
CHEX1	JSR GETHEX	;GET HEX VALUE
	BCS CHEX2
	LDY #0
	STA (PNT),Y	;REPLACE
	INC PNT
	BNE CHEX1
	INC PNT+1
	BNE CHEX1
	LDA #TOOMCH	;TOO MUCH PARAMETERS
	SEC
	RTS

CHEX2	CMP #MISPAR
	BNE CHANGX
	CLC
	RTS

CASC	JSR GETHEX	;GET DMA POINTER
	BCS CHANGX
	STY PNT+1
	STA PNT
CASC1	JSR GNCHR	;GET HEX VALUE
	BCS CHANGX
	LDY #0
	STA (PNT),Y	;REPLACE
	INC PNT
	BNE CASC1
	INC PNT+1
	BNE CASC1
	LDA #TOOMCH	;TOO Many PARAMETERS
	SEC
	RTS


DUMP	JSR GETHEX	;DUMP
	STA PNT
	STY PNT+1
	BCC DUMP4
	CMP #MISPAR
	BNE DUMP2	;default is buffer
	LDA BUFFER
	STA PNT
	LDA BUFFER+1
	STA PNT+1
DUMP4	LDA PNT
	AND #$F0
	STA PNT
	JSR GETHEX	;GET 2. PARAMETER
	STA ENDAD
	STY ENDAD+1
	BCC DUMP1
	CMP #MISPAR
	BNE DUMP2
	CLC
	LDA PNT
	ADC #$E0	;14 LINES DEFAULT
	STA ENDAD
	LDA PNT+1
	ADC #0
	STA ENDAD+1
DUMP1	JSR DUMPHE	;PRINT HEADER
DUMP3	JSR DUMPL	;PRINT LINE
	BCC DUMP3
	JSR FIXCUR
DUMPX	RTS

DUMP2	SEC
	RTS


FILL	JSR G1ADR	;FILL MEMORY
	BCS FILLX
	JSR GETHEX	;GET 2. ARGUMENT
	BCS FILLX
	STA ENDAD
	STY ENDAD+1
	JSR GETHEX	;GET BYTE
	BCS FILLX
FILL1	JSR CHKEND
	BCS FILL2
	LDY #0
	STA (PNT),Y
	INC PNT
	BNE FILL1
	INC PNT+1
	BNE FILL1

FILL2	CLC
FILLX	RTS


MOVE	JSR G1ADR	;MOVE TO 1. ARGUMENT
	BCS MOVEX
	JSR GETHEX	;GET 2. ARGUMENT START
	BCS MOVEX
	STA BEGAD
	STY BEGAD+1
	JSR GETHEX	;GET 3. ARGUMENT END
	BCS MOVEX
	STA ENDAD
	STY ENDAD+1
	CPY CURAD+1	;ENDAD<CURAD?
	BCC MOVE1
	CMP CURAD
	BCC MOVE1
	LDA CURAD+1	;BEGAD>CURAD?
	CMP BEGAD+1
	BCC MOVE1
	LDA CURAD
	CMP BEGAD
	BCC MOVE1
	LDA #M_OVERLAP
	RTS

MOVE1	LDA PNT+2
	PHA
	LDA PNT+3
	PHA
	LDY #0
	LDA BEGAD
	STA PNT+2
	LDA BEGAD+1
	STA PNT+3
MOVE2	LDA (PNT+2),Y
	STA (PNT),Y
	LDA PNT+3	;CHECK FOR PNT+2=ENDAD
	CMP ENDAD+1
	BCC MOVE5
	LDA PNT+2
	CMP ENDAD
	BCS MOVE3
MOVE5	INC PNT		;INC PNT
	BNE MOVE4
	INC PNT+1
MOVE4	INC PNT+2	;INC PNT+2
	BNE MOVE2
	INC PNT+3
	JMP MOVE2

MOVE3	PLA		;RESTORE PNT+2
	STA PNT+3
	PLA
	STA PNT+2
	CLC
MOVEX	RTS


